import { createServerSupabaseClient } from '@/lib/supabase/server';
import { NextResponse } from 'next/server';
import type { EmailOtpType } from '@supabase/supabase-js';

/**
 * Auth Callback Route
 * Verarbeitet Tokens aus Supabase-Links (Einladungen, Passwort-Reset, Magic Links, etc.)
 */
export async function GET(request: Request) {
  const { searchParams, origin } = new URL(request.url);
  const code = searchParams.get('code');
  const token_hash = searchParams.get('token_hash');
  const type = searchParams.get('type') as EmailOtpType | null;
  const next = searchParams.get('next') ?? '/auth/reset-password';

  const supabase = await createServerSupabaseClient();

  // Method 1: Authorization Code (Einladungen, etc.)
  if (code) {
    const { error } = await supabase.auth.exchangeCodeForSession(code);

    if (!error) {
      return NextResponse.redirect(`${origin}${next}`);
    }

    console.error('[Auth Callback] Code exchange error:', error.message);
  }

  // Method 2: Token Hash (Magic Links, Email OTP)
  if (token_hash && type) {
    const { error } = await supabase.auth.verifyOtp({
      token_hash,
      type,
    });

    if (!error) {
      return NextResponse.redirect(`${origin}${next}`);
    }

    console.error('[Auth Callback] Token verification error:', error.message);
  }

  // Bei Fehler oder fehlenden Parametern zur Login-Seite
  return NextResponse.redirect(`${origin}/auth/login?error=invalid_token`);
}
